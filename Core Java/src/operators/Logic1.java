package operators;

public class Logic1 
{

	public static void main(String[] args)
	
	{
		boolean b1=true; 
		boolean b2=false;
		
		//LOgical And &&
		System.out.println("b1&&b2: " +(b1&&b2)  );
		//Logical OR ||
		System.out.println("b1||b2: " +(b1||b2)  );
		//Logical Not !
		
		System.out.println("b1!b2: " + !(b1&&b2)  );

		
	}

}
